# MATH FUNCTIONS WITH NUMBERS:

import math
# These are some functions used working with number.

print(round(5.7))  # output> 6
print(abs(-5.7))  # output> 5.7

# to use more functions to work with numbers in mathematical way
# "import math" this module will allow us to use more different
# functions in our python Code.
# To use it:

print(math.ceil(8.7))  # output> 9
print(math.fabs(8))  # output> 8.0
print(math.factorial(5))  # output> 120
print(math.tan(1))  # output> 1.557407
print(math.log(25))  # output> 3.218875
